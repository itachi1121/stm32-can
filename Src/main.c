void servo_locate_mode_set(uint8_t id, int32_t location, int32_t speed)
{
			int ubCounter = 0;
			uint8_t send_message_mode[8] = {0x2F, 0x60, 0x60, 0x00, 0x01, 0x00, 0x00, 0x00};		//工作模式指令		
			uint8_t send_message_locate[8] = {0x23, 0x7A, 0x60, 0x00, 0x50, 0xC3, 0x00, 0x00};  //目标位置指令
			uint8_t send_message_speed[8] = {0x23, 0x81, 0x60, 0x00, 0x55, 0x55, 0x08, 0x00};   //梯形速度指令
			uint8_t send_message_control[8] = {0x2B, 0x40, 0x60, 0x00, 0x3F, 0x10, 0x00, 0x00}; //控制字指令
			
			//位置 赋值
			send_message_locate[4] = location & 0xff;
			send_message_locate[5] = (location >> 8) & 0xff;
			send_message_locate[6] = (location >> 16) & 0xff;
			send_message_locate[7] = (location >> 24) & 0xff;
			
			//梯形速度 赋值
			uint32_t temp_value = speed * 512 * 10000 / 1875;
			send_message_speed[4] = temp_value & 0xff;
			send_message_speed[5] = (temp_value >> 8) & 0xff;
			send_message_speed[6] = (temp_value >> 16) & 0xff;
			send_message_speed[7] = (temp_value >> 24) & 0xff;
			
			uint32_t std_ID = 0x600 | id;  //CAN 发送给舵机的标准ID
	
			//数据帧
			TxHeader.RTR = CAN_RTR_DATA;
			//帧类型为标准帧
			TxHeader.IDE = CAN_ID_STD;			
			TxHeader.StdId=std_ID;
			TxHeader.TransmitGlobalTime = DISABLE;
			TxHeader.DLC = 8;
		
			if (HAL_CAN_AddTxMessage(&hcan1, &TxHeader, send_message_mode, &TxMailbox) != HAL_OK)
      {
          /* Transmission request Error */
         Error_Handler();
      }
			HAL_Delay(1);
			if (HAL_CAN_AddTxMessage(&hcan1, &TxHeader, send_message_speed, &TxMailbox) != HAL_OK)
      {
          /* Transmission request Error */
         Error_Handler();
      }
			HAL_Delay(1);
	//		printf("locate %d\n", location);

			if (HAL_CAN_AddTxMessage(&hcan1, &TxHeader, send_message_locate, &TxMailbox) != HAL_OK)
      {
          /* Transmission request Error */
         Error_Handler();
      }
			HAL_Delay(1);
			
			if (HAL_CAN_AddTxMessage(&hcan1, &TxHeader, send_message_control, &TxMailbox) != HAL_OK)
      {
          /* Transmission request Error */
         Error_Handler();
      }
			HAL_Delay(1);
}


